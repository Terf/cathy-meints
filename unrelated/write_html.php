function find_performer($key, $arr) {
	$ret = ['songs' => [], 'files' => []];
	foreach ($arr as $k => $v) {
		if (in_array($key, explode(' ', str_replace(',', '', $v[0])))) {
			$ret['songs'][] = $k;
			$ret['files'][] = $v[1];
		}
	}
	return $ret;
}



